# Some extra stuff with lists

list1 = ['a', 'b', 1, 2]
print('Original list: ', list1)

#pop will remove last elements from your list
list1.pop()
print("List after pop: ", list1)

#same here
list1.pop()
print('List after pop again: ', list1)

#append will add elements at last in your list which you will give
list1.append('value1')
print('List after append: ', list1)

list1.append(23)
print('List after append again: ', list1)